type Translations = (typeof TRANSLATIONS)[Language];

type LanguageContextValue = {
  lang: Language;
  setLang: (l: Language) => void;
  toggleLang: () => void;
  t: Translations;
  fetchAndApplyServerLang: () => Promise<void>;
  guideLang: Language;
  setGuideLang: (l: Language) => void;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

function detectInitialLang(): Language {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (isLanguage(stored)) return stored;
  const browser = window.navigator.language?.toLowerCase() ?? "";
  if (browser.startsWith("es")) return "es";
  if (browser.startsWith("pt")) return "pt";
  return "en";
}

function patchServerLang(lang: Language): void {
  const token = getToken();
  if (!token) return;
  fetch(PREFS_URL, {
    method: "PATCH",
    headers: { "Content-Type": "application/json", Authorization: `Bearer ${token}` },
    body: JSON.stringify({ dashboardLanguage: lang }),
  }).catch(() => {});
}

function detectInitialGuideLang(): Language {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(GUIDE_LANG_KEY);
  if (isLanguage(stored)) return stored;
  return "en";
}

function patchServerGuideLang(lang: Language): void {
  const token = getToken();
  if (!token) return;
  fetch(PREFS_URL, {
    method: "PATCH",
    headers: { "Content-Type": "application/json", Authorization: `Bearer ${token}` },
    body: JSON.stringify({ guideLanguage: lang }),
  }).catch(() => {});
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => detectInitialLang());
  const [guideLang, setGuideLangState] = useState<Language>(() => detectInitialGuideLang());
  const hydratedRef = useRef(false);

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, lang);
      document.documentElement.lang = lang;
    }
  }, [lang]);

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(GUIDE_LANG_KEY, guideLang);
    }
  }, [guideLang]);

  const fetchAndApplyServerLang = useCallback(async () => {
    const token = getToken();
    if (!token) {
      hydratedRef.current = true;
      return;
    }
    try {
      const res = await fetch(PREFS_URL, {
        headers: { Authorization: `Bearer ${token}` },
      });
      if (res.ok) {
        const data = (await res.json()) as { dashboardLanguage?: string | null; guideLanguage?: string | null };
        const serverLang = data.dashboardLanguage;
        if (isLanguage(serverLang)) {
          setLangState(serverLang);
        }
        const serverGuideLang = data.guideLanguage;
        if (isLanguage(serverGuideLang)) {
          setGuideLangState(serverGuideLang);
        }
      }
    } catch {
    }
    hydratedRef.current = true;
  }, []);

  const setLang = (l: Language) => {
    setLangState(l);
    if (hydratedRef.current) {
      patchServerLang(l);
    }
  };

  const setGuideLang = (l: Language) => {
    setGuideLangState(l);
    patchServerGuideLang(l);
  };

  const toggleLang = () => {
    setLangState((prev) => {
      const next = SUPPORTED_LANGS[(SUPPORTED_LANGS.indexOf(prev) + 1) % SUPPORTED_LANGS.length];
      if (hydratedRef.current) {
        patchServerLang(next);
      }
      return next;
    });
  };

  const value: LanguageContextValue = {
    lang,
    setLang,
    toggleLang,
    t: TRANSLATIONS[lang],
    fetchAndApplyServerLang,
    guideLang,
    setGuideLang,
  };

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used inside <LanguageProvider>");
  return ctx;
}
